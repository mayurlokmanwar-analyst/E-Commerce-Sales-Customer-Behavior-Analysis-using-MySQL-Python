SELECT 
ROUND(
    (SUM(CASE WHEN payment_installments > 1 THEN 1 ELSE 0 END) * 100.0) / COUNT(*),
2
) AS installment_percentage
FROM payments;

# 5. Count the number of customers from each state
SELECT customer_state, COUNT(customer_id) AS customer_count
FROM customers
