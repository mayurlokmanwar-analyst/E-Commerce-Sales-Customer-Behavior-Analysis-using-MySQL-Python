create database ecommerce;

# Connecting with the database
use ecommerce;

# 1. # Top 10 cities by  the number of customers
SELECT customer_city, COUNT(*) as customer_count
FROM customers
GROUP BY customer_city
ORDER BY customer_count DESC
LIMIT 10;

# 2. Count the number of orders placed in 2018
SELECT COUNT(order_id) AS orders_in_2018
FROM orders
WHERE YEAR(order_purchase_timestamp) = 2018;

# 3. Find the total sales generated across product categories to identify top performing categories
SELECT 
    UPPER(products.product_category) AS category,
    ROUND(SUM(payments.payment_value), 2) AS sales
FROM products
JOIN order_items
    ON products.product_id = order_items.product_id
JOIN payments
    ON payments.order_id = order_items.order_id
WHERE products.product_category IS NOT NULL
GROUP BY category
ORDER BY sales DESC
LIMIT 10;

